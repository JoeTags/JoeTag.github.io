// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(num) {
if (num === 0) {
	return true;
} else if (num === 1) {
	 return false;
} else if (num < 0) {
  return false;
} else {
  return isEven(num - 2); 

}
};

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(string, character) {
let count = 0;
  for (let i = 0; i < string.length; i++) {
  if (string[i].includes(character)) {
  count = count + 1;
  }
}
      
return count;
      }

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(string) {
let count = 0;
  for (let i = 0; i < string.length; i++) {
  if (string[i].includes("B")) {
  count = count + 1;
  }
}
      
return count;
      }

